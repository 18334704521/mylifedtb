package com.electroline.myapp.domain;

public class CAdviceAttachment {
	private int id;
	private String fileName;
	private int cAdviceId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public int getcAdviceId() {
		return cAdviceId;
	}
	public void setcAdviceId(int cAdviceId) {
		this.cAdviceId = cAdviceId;
	}
	
	
}

package com.electroline.myapp.domain;

public class JavaAdviceAttachment {
	private int id;
	private String fileName;
	private int javaAdviceId;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public int getJavaAdviceId() {
		return javaAdviceId;
	}
	public void setJavaAdviceId(int javaAdviceId) {
		this.javaAdviceId = javaAdviceId;
	}
	
	
}

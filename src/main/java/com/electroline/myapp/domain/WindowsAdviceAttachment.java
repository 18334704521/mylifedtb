package com.electroline.myapp.domain;

public class WindowsAdviceAttachment {
	private int id;
	private String fileName;
	private int windowsAdviceId;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public int getWindowsAdviceId() {
		return windowsAdviceId;
	}
	public void setWindowsAdviceId(int windows_advice_id) {
		this.windowsAdviceId = windows_advice_id;
	}
	
	
}

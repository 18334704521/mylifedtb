package com.electroline.myapp.domain;

public class ElectronicsGeneralAdviceAttachment {
	private int id;
	private String fileName;
	private int electronicsGeneralAdviceId;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public int getElectronicsGeneralAdviceId() {
		return electronicsGeneralAdviceId;
	}
	public void setElectronicsGeneralAdviceId(int electronicsGeneralAdviceId) {
		this.electronicsGeneralAdviceId = electronicsGeneralAdviceId;
	}
	
}

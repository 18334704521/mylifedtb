package com.electroline.myapp.domain;

public class MicroprocessorAdviceAttachment {
	private int id;
	private String fileName;
	private int microprocessorAdviceId;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public int getMicroprocessorAdviceId() {
		return microprocessorAdviceId;
	}
	public void setMicroprocessorAdviceId(int microprocessorAdviceId) {
		this.microprocessorAdviceId = microprocessorAdviceId;
	}

}

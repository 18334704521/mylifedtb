package com.electroline.myapp.domain;

public class EclipseAdviceAttachment {
	private int id;
	private String fileName;
	private int eclipseAdviceId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public int getEclipseAdviceId() {
		return eclipseAdviceId;
	}
	public void setEclipseAdviceId(int eclipseAdviceId) {
		this.eclipseAdviceId = eclipseAdviceId;
	}
	
	
}
